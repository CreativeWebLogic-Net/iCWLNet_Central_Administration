        <p>Poggenpohl  innovation has seen the introduction of the Green Kitchen - kitchens that  embody recycling and environmental features.<strong> </strong>Some of these concepts have now become law in Europe.</p>
        <p>The Green kitchen  concept, involves layers of reuse, recycling and environmentally friendly  products based on clean and efficient manufacturing. </p>
        <p>The Green kitchen  is designed, manufactured and installed to allow the kitchen to be  de-installed, relocated and reused in a new location - the &quot;Green&quot;  kitchen must be well made and engineered to last.</p>
        <p>Poggenpohl lead the world in the  introduction of low formaldehyde materials for kitchens. European authorities  recognised the health dangers from formaldehyde and have now introduced strict  standards. </p>
        <p>Poggenpohl kitchens meet the strict E (1)  classification for outgassing of chemicals.</p>
        <p>Poggenpohl have lead the way with the use  of PVC free materials to minimise the risk of chlorine release into the  atmosphere and to address health concerns about human impacts of PVC  plasticisers. </p>
        <p>Recyclable food  grade plastics have replaced PVC - ABS, polypropylene and PET are used  extensively. ABS for cutlery inserts, polypropylene for carcass edges and leg  systems, PET for polymer finishes. </p>
        <p>Water base paint  systems have replaced hydrocarbon-based polyurethane resulting in greatly  reduced waste and less pollution of the atmosphere. </p>
        <p>Poggenpohl have  certification for compliance with DIN EN ISO 14001 for sustainable products.  Only certified plantation timbers are used &ndash; no rainforest products.</p>
        <p>Aluminium is  replacing steel - it doesn't rust - and while it has a high initial energy  content most of this is recovered when the aluminium is recycled.</p>
        Poggenpohl believe that the  materials of the kitchen should be able to be recovered and reprocessed into  new materials at the end of the life of the kitchen.
